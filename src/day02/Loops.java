package day02;

public class Loops {
    public static void main(String[] args) {
        //I have picked this topic for practicing

        long a = 10L;

        switch(a){
            case 10:
                System.out.println("10 is long type");
                break;
            case 11:
                System.out.println("there is no such a number");
                break;
            default:
                System.out.println("Make sure switch accept correct data type");
        }

    }


}
